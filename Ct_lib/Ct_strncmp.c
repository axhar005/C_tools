#include "Ctlib.h"

/// @brief compere 2 str
/// @param s1 
/// @param s2 
/// @param n size in byte
/// @return return the diference
int	Ct_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	if (n == 0 || !s1 || !s2)
		return (0);
	i = 0;
	while ((s1[i] && s2[i]) && s1[i] == s2[i] && i < n - 1)
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
